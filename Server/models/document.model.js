import mongoose from "mongoose";

const DocumentSchema = new mongoose.Schema(
  {
    doctitle: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      trim: true,
    },
    fileUrl: {
      type: String,
      required: true,
    },
    course: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Course",
      required: true,
    },
  },
  { timestamps: true }
);

export const Document = mongoose.model("Document", DocumentSchema);

